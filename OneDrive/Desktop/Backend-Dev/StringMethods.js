/**String Methods

Method
Example

length
Returns length of a string
'hello'.length → 5

charAt(index)
Returns character at index
'hello'.charAt(1) → 'e'

indexOf(substr)

First occurrence index

'hello'.indexOf('l') → 2

lastIndexOf(substr)

Last occurrence index

'hello'.lastIndexOf('l') → 3

includes(substr)

Returns true if found

'hello'.includes('ll') → true

startsWith(prefix)

Checks start

'hello'.startsWith('he') → true

endsWith(suffix)

Checks end

'hello'.endsWith('o') → true

slice(start, end)

Extracts part of string

'hello'.slice(1, 4) → 'ell'

substring(start, end)

Like slice

'hello'.substring(1, 4) → 'ell'

toUpperCase()

Converts to UPPERCASE

'hello'.toUpperCase() → 'HELLO'

toLowerCase()

Converts to lowercase

'HELLO'.toLowerCase() → 'hello'

trim()

Removes spaces

' hi '.trim() → 'hi'

split(separator)

Splits into array

'a,b,c'.split(',') → ['a','b','c']

replace(find, replace)

Replaces part

'hi tom'.replace('tom','sam') → 'hi sam'  **/